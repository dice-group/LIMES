package org.aksw.limes.core.measures.mapper.topology.cobalt.diagonal;

import org.aksw.limes.core.io.cache.ACache;
import org.aksw.limes.core.io.cache.MemoryCache;
import org.aksw.limes.core.io.mapping.AMapping;
import org.aksw.limes.core.io.mapping.MappingFactory;
import org.aksw.limes.core.measures.mapper.AMapper;
import org.junit.Test;

import static org.junit.Assert.assertEquals;

public class CobaltDiagonalCoversMapperTest {

    @Test
    public void testGetMapping() throws Exception {
        ACache s = new MemoryCache();
        s.addTriple("http://test.com/s/#1", "asWKT", "POLYGON ((-10 -10, 0 10, 10 10, 10 0, -10 -10))");
        s.addTriple("http://test.com/s/#2", "asWKT", "POLYGON ((-20 -20, -10 0, 0 0, 0 -10, -20 -20))");
        ACache t = new MemoryCache();
        t.addTriple("http://test.com/t/#1", "asWKT", "POLYGON ((-8 -8, 2 8, 8 8, 8 2, -8 -8))");
        t.addTriple("http://test.com/t/#2", "asWKT", "POLYGON ((-10 -10, 0 10, 10 10, 10 0, -10 -10))");
        AMapping referenceMapping = MappingFactory.createMapping(MappingFactory.MappingType.MEMORY_MAPPING);
        referenceMapping.add("http://test.com/s/#1", "http://test.com/t/#1", 1.0d);
        referenceMapping.add("http://test.com/s/#1", "http://test.com/t/#2", 1.0d);
        AMapper mapper = new CobaltDiagonalCoversMapper();
        assertEquals("Expect mapping generated by CobaltDiagonalCoversMapper to be equal to reference mapping",
                mapper.getMapping(s, t, "?x", "?y", "top_cobalt_diagonal_covers(x.asWKT, y.asWKT)", 1.0d), referenceMapping);
    }
}